package TurnQuest.view.reports;

public class ReportGroupDivision {
    private String rptGroupDivCode;
    private String rptGroupCode;
    private String divCode;
    private String divName;
    private String divOrder;

    public void setRptGroupDivCode(String rptGroupDivCode) {
        this.rptGroupDivCode = rptGroupDivCode;
    }

    public String getRptGroupDivCode() {
        return rptGroupDivCode;
    }

    public void setRptGroupCode(String rptGroupCode) {
        this.rptGroupCode = rptGroupCode;
    }

    public String getRptGroupCode() {
        return rptGroupCode;
    }

    public void setDivCode(String divCode) {
        this.divCode = divCode;
    }

    public String getDivCode() {
        return divCode;
    }

    public void setDivName(String divName) {
        this.divName = divName;
    }

    public String getDivName() {
        return divName;
    }

    public void setDivOrder(String divOrder) {
        this.divOrder = divOrder;
    }

    public String getDivOrder() {
        return divOrder;
    }
}
